const os = require('os');

// Platform
console.log(os.platform());

//CPU Arch
console.log(os.arch());

// Free memory
console.log(os.freemem());

// Total memory
console.log(os.totalmem());

//CPU Core Info
console.log(os.cpus());

//Home dir
console.log(os.homedir());

// Uptime
console.log(os.uptime());